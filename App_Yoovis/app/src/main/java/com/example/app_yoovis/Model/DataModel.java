package com.example.app_yoovis.Model;

public class DataModel {
    private String message;
    private int id;
    private String merk_hp;
    private String tipe_hp;
    private String kerusakan_hp;
    private String total;

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMerk_hp() {
        return merk_hp;
    }

    public void setMerk_hp(String merk_hp) {
        this.merk_hp = merk_hp;
    }

    public String getTipe_hp() {
        return tipe_hp;
    }

    public void setTipe_hp(String tipe_hp) {
        this.tipe_hp = tipe_hp;
    }

    public String getKerusakan_hp() {
        return kerusakan_hp;
    }

    public void setKerusakan_hp(String kerusakan_hp) {
        this.kerusakan_hp = kerusakan_hp;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
